    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        con = new Coneccion();

         reg = con.getConnection();
         
         String rfc = jTextField1.getText();
         String apat = jTextField2.getText();
         String amat = jTextField3.getText();
         String nom = jTextField4.getText();
         String ced = jTextField5.getText();
         String dir = jTextField6.getText();
         String sexo = (String)jComboBox1.getSelectedItem();
         String grupo = jTextField8.getText();
         String contra = jTextField7.getText();
         
        String sql = "INSERT INTO Profesor(RFC,Ap_paterno,Ap_materno,Nombre,Cedula_prof,direccion,sexo,grupo,contrasenia) VALUES (?,?,?,?,?,?,?,?,?)";
        try {
            PreparedStatement pst = (PreparedStatement) reg.prepareStatement(sql);
            pst.setString(1, rfc);
            pst.setString(2, apat);
            pst.setString(3, amat);
            pst.setString(4, nom);
            pst.setString(5, ced);
            pst.setString(6, dir);
            pst.setString(7, sexo);
            pst.setString(8, grupo);
            pst.setString(9, contra);
            
            pst.executeUpdate();
            
        } catch (SQLException ex) {
            Logger.getLogger(captura_profes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

